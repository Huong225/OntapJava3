    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
        if (this.txtId.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập mã sinh viên");
            this.txtId.requestFocus();
            return;
        } else {
            Grade gr = this.grade.getMaSV(this.txtId.getText());
            if (gr != null) {
                this.lblName.setText(gr.getSt().getName());
                this.txtMaSV.setText(gr.getSt().getMaSV());
                this.txtAnh.setText(gr.getDiemTA() + "");
                this.txtTin.setText(gr.getDiemTH() + "");
                this.txtGDTC.setText(gr.getDiemGDTC() + "");
                String tb = String.format("%.1f", gr.getDiemTB());
                this.lblAvg.setText(tb);
            }
        }
    }//GEN-LAST:event_btnSearchActionPerformed

