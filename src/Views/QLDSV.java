    public Grade AddDiem() {
        Grade gr = new Grade();
        gr.setSt(new Student(this.txtMaSV.getText()));
        gr.setDiemTA(Float.parseFloat(this.txtAnh.getText()));
        gr.setDiemTH(Float.parseFloat(this.txtTin.getText()));
        gr.setDiemGDTC(Float.parseFloat(this.txtGDTC.getText()));
        gr.setDiemTB((Float.parseFloat(this.txtAnh.getText()) + Float.parseFloat(this.txtAnh.getText()) + Float.parseFloat(this.txtGDTC.getText())) / 3);

        return gr;
    }
    
    private boolean Check() {
        // check infor
        try {
            if (this.lblName.getText().trim().equals("") && this.txtMaSV.getText().trim().equals("")) {
                JOptionPane.showMessageDialog(this, "Vui lòng nhập tên và mã sinh viên ");
                return false;
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
        // check điểm TA
        try {
            if (this.txtAnh.getText().trim().equals("")) {
                JOptionPane.showMessageDialog(this, "Vui lòng nhập điểm Tiếng Anh");
                this.txtAnh.requestFocus();
                return false;
            }
            float diem = Float.parseFloat(this.txtAnh.getText());
            if (diem < 0 || diem > 10) {
                JOptionPane.showMessageDialog(this, "Điểm phải từ 0 - 10");
                this.txtAnh.requestFocus();
                return false;
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Điểm phải là số");
            this.txtAnh.requestFocus();
            return false;
        }

        try {
            if (this.txtTin.getText().trim().equals("")) {
                JOptionPane.showMessageDialog(this, "Vui lòng nhập điểm Tin");
                this.txtTin.requestFocus();
                return false;
            }
            float diem = Float.parseFloat(this.txtTin.getText());
            if (diem < 0 || diem > 10) {
                JOptionPane.showMessageDialog(this, "Điểm phải từ 0 - 10");
                this.txtTin.requestFocus();
                return false;
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Điểm phải là số");
            this.txtTin.requestFocus();
            return false;
        }

        try {
            if (this.txtGDTC.getText().trim().equals("")) {
                JOptionPane.showMessageDialog(this, "Vui lòng nhập điểm GDTC");
                this.txtGDTC.requestFocus();
                return false;
            }
            float diem = Float.parseFloat(this.txtGDTC.getText());
            if (diem < 0 || diem > 10) {
                JOptionPane.showMessageDialog(this, "Điểm phải từ 0 - 10");
                this.txtGDTC.requestFocus();
                return false;
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Điểm phải là số");
            this.txtGDTC.requestFocus();
            return false;
        }
        return true;
    }


    private void fillTable() {
        this.listG = this.grade.getTOP3();
        this.model.setRowCount(0);
        for (Grade x : listG) {
            model.addRow(new Object[]{x.getSt().getMaSV(), x.getSt().getName(),
                x.getDiemTA(), x.getDiemTH(), x.getDiemGDTC(), x.getDiemTB()});
        }
    }

    public void display(int index) {
        if (this.index >= 0) {
            Grade gr = listG.get(index);
            this.txtMaSV.setText(gr.getSt().getMaSV());
            this.lblName.setText(gr.getSt().getName());
            this.txtAnh.setText(gr.getDiemTA() + "");
            this.txtTin.setText(gr.getDiemTH() + "");
            this.txtGDTC.setText(gr.getDiemGDTC() + "");
            String tb = String.format("%.1f", gr.getDiemTB());
            this.lblAvg.setText(tb);
            this.tblQLDSV.setRowSelectionInterval(index, index);
        }
    }
    
    private void Update() {
        String maSV = this.txtId.getText();
        float diemTA = Float.parseFloat(this.txtAnh.getText());
        float diemTin = Float.parseFloat(this.txtTin.getText());
        float diemGDTC = Float.parseFloat(this.txtGDTC.getText());
        float diemTB = (Float.parseFloat(this.txtAnh.getText()) + Float.parseFloat(this.txtTin.getText()) + Float.parseFloat(this.txtGDTC.getText())) / 3;
        Grade g = new Grade(new Student(maSV), diemTA, diemTin, diemGDTC, diemTB);
        this.grade.update(g);
    }

